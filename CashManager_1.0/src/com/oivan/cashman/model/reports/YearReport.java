package com.oivan.cashman.model.reports;

public class YearReport {
	
	private int yearReportId;
	private int userId;
	private int monthReportId;
	
	public int getYearReportId() {
		return yearReportId;
	}
	public void setYearReportId(int yearReportId) {
		this.yearReportId = yearReportId;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public int getMonthReportId() {
		return monthReportId;
	}
	public void setMonthReportId(int monthReportId) {
		this.monthReportId = monthReportId;
	}	
}
